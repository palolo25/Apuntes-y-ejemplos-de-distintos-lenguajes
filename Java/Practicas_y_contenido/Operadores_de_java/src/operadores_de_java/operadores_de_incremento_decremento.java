package operadores_de_java;

public class operadores_de_incremento_decremento {

    public static void main(String[] args) {
        
        int x = 5, y;
        y = --x;
        x++;// x += 1;
        x--;// x -= 1;
        
        System.out.println(x);
        System.out.println(y);
        

    }

}
